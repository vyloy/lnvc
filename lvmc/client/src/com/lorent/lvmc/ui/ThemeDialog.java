    private void setTheme(DockingWindowsTheme theme,java.awt.event.MouseEvent evt) throws Exception{
        if(evt.getButton()==MouseEvent.BUTTON1 && (evt.getClickCount()==1||evt.getClickCount()==2)){
            ViewManager.changeTheme(theme);
            this.dispose();;
        }
    }
    
