package com.qd.bean;

public class TunerParam
{	
	private int ber;
	private int signal_quality;
	private int signal_strength;
	
	public int getBer()
	{
		return ber;
	}
	
	public void setBer(int ber)
	{
		this.ber = ber;
	}
	
	public int getSignal_quality()
	{
		return signal_quality;
	}
	
	public void setSignal_quality(int signalQuality)
	{
		signal_quality = signalQuality;
	}
	
	public int getSignal_strength()
	{
		return signal_strength;
	}
	
	public void setSignal_strength(int signalStrength)
	{
		signal_strength = signalStrength;
	}
	

}
