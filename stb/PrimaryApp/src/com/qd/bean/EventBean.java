package com.qd.bean;

public class EventBean
{
	private int eventYear;
	private int eventMonth;
	private int eventDay;
	private int eventStartTime;
	private int eventDuration;
	private int eventId;
	private int serId;
	private String eventName;
	private int eventOrderWay;
	
	public int getEventYear()
	{
		return eventYear;
	}
	
	public void setEventYear(int eventYear)
	{
		this.eventYear = eventYear;
	}
	
	public int getEventMonth()
	{
		return eventMonth;
	}
	
	public void setEventMonth(int eventMonth)
	{
		this.eventMonth = eventMonth;
	}
	
	public int getEventDay()
	{
		return eventDay;
	}
	
	public void setEventDay(int eventDay)
	{
		this.eventDay = eventDay;
	}
	
	public int getEventStartTime()
	{
		return eventStartTime;
	}
	
	public void setEventStartTime(int eventStartTime)
	{
		this.eventStartTime = eventStartTime;
	}
	
	public int getEventDuration()
	{
		return eventDuration;
	}
	
	public void setEventDuration(int eventDuration)
	{
		this.eventDuration = eventDuration;
	}
	
	public int getEventId()
	{
		return eventId;
	}
	
	public void setEventId(int eventId)
	{
		this.eventId = eventId;
	}
	
	public int getSerId()
	{
		return serId;
	}
	
	public void setSerId(int serId)
	{
		this.serId = serId;
	}
	
	public String getEventName()
	{
		return eventName;
	}
	
	public void setEventName(String eventName)
	{
		this.eventName = eventName;
	}
	
	public int getEventOrderWay()
	{
		return eventOrderWay;
	}
	
	public void setEventOrderWay(int eventOrderWay)
	{
		this.eventOrderWay = eventOrderWay;
	}

}
